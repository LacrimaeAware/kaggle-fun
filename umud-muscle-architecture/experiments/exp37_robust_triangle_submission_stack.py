"""Stack the isolated robust-triangle geometry delta onto the current best public-LB anchor.

The isolated robust-triangle file is generated by:

    UMUD_TOP_BOUNDARY_MODE=robust_triangle \
    UMUD_LOCAL_OUT=results/submission_robust_triangle_only.csv \
    UMUD_LOCAL_DEBUG_OUT=results/calibration_debug_robust_triangle_only.csv \
    python local_infer.py

This script keeps the current best temporal/subpixel/shape/OCR stack and adds only the
robust-triangle delta relative to the protected 0.619 baseline.
"""

from __future__ import annotations

from pathlib import Path

import pandas as pd

ROOT = Path(__file__).resolve().parent.parent
RESULTS = ROOT / "results"

BASE = RESULTS / "submission_local.csv"
CURRENT = RESULTS / "submission_burn_13_temporal_subpixel_shape_img00275_ocr_scale.csv"
ROBUST = RESULTS / "submission_robust_triangle_only.csv"
OUT = RESULTS / "submission_burn_15_temporal_subpixel_shape_ocr_robust_triangle.csv"
SUMMARY = RESULTS / "submission_burn_15_robust_triangle_summary.csv"

TOL = {"pa_deg": 6.0, "fl_mm": 12.0, "mt_mm": 3.0}
RANGES = {"pa_deg": (5.0, 45.0), "fl_mm": (30.0, 200.0), "mt_mm": (10.0, 50.0)}


def read_submission(path: Path) -> pd.DataFrame:
    df = pd.read_csv(path)
    cols = ["image_id", "pa_deg", "fl_mm", "mt_mm"]
    if list(df.columns) != cols:
        raise SystemExit(f"{path}: expected {cols}, got {list(df.columns)}")
    if len(df) != 309 or df["image_id"].duplicated().any():
        raise SystemExit(f"{path}: invalid row count or duplicate IDs")
    return df


def assert_same_ids(*dfs: pd.DataFrame) -> None:
    ids = list(dfs[0]["image_id"])
    for df in dfs[1:]:
        if list(df["image_id"]) != ids:
            raise SystemExit("image_id order mismatch")


def movement(ref: pd.DataFrame, cand: pd.DataFrame, name: str, path: Path) -> dict:
    row = {"candidate": name, "path": str(path)}
    total = 0.0
    for col in ("pa_deg", "fl_mm", "mt_mm"):
        d = (cand[col] - ref[col]).abs()
        row[f"{col}_changed"] = int((d > 1e-9).sum())
        row[f"{col}_mean_abs"] = float(d.mean())
        row[f"{col}_p95_abs"] = float(d.quantile(0.95))
        row[f"{col}_max_abs"] = float(d.max())
        total += float((d / TOL[col]).mean()) / 3.0
    row["mean_normalized_row_movement_vs_current"] = total
    row["fl_mean"] = float(cand["fl_mm"].mean())
    row["fl_std"] = float(cand["fl_mm"].std())
    row["mt_mean"] = float(cand["mt_mm"].mean())
    row["mt_std"] = float(cand["mt_mm"].std())
    return row


def main() -> None:
    base = read_submission(BASE)
    current = read_submission(CURRENT)
    robust = read_submission(ROBUST)
    assert_same_ids(base, current, robust)

    out = current.copy()
    for col in ("pa_deg", "fl_mm", "mt_mm"):
        out[col] = current[col] + (robust[col] - base[col])
        lo, hi = RANGES[col]
        out[col] = out[col].clip(lo, hi).round(3)
    out.to_csv(OUT, index=False)

    summary = pd.DataFrame([
        movement(base, robust, "isolated_robust_triangle_vs_0619", ROBUST),
        movement(current, out, "stacked_robust_triangle_vs_0589_current", OUT),
    ])
    summary.to_csv(SUMMARY, index=False)
    print(summary.to_string(index=False))
    print(f"\nwrote:\n  {OUT}\n  {SUMMARY}")


if __name__ == "__main__":
    main()
