def main() -> None:
    cd = build_card_data()
    decks = json.loads(DECKS.read_text(encoding="utf-8")) if DECKS.exists() else []
    replays = json.loads(REPLAYS.read_text(encoding="utf-8")) if REPLAYS.exists() else []
    html = (HTML
            .replace("__CARDS__", json.dumps(cd, ensure_ascii=False, separators=(",", ":")))
            .replace("__DECKS__", json.dumps(decks, ensure_ascii=False, separators=(",", ":")))
            .replace("__REPLAYS__", json.dumps(replays, ensure_ascii=False, separators=(",", ":")))
            .replace("__IMG__", IMG_REL))
    OUT.write_text(html, encoding="utf-8")
    kb = len(html.encode("utf-8")) // 1024
    print(f"wrote {OUT.relative_to(ROOT)} ({kb} KB, {len(cd)} cards, {len(decks)} decks, {len(replays)} replays)")


if __name__ == "__main__":
    main()
