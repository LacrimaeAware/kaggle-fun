def build_card_data() -> dict:
    have_img = set()
    if MANIFEST.exists():
        have_img = {int(k) for k in json.loads(MANIFEST.read_text(encoding="utf-8")).keys()}
    out = {}
    for cid, c in cards._load().items():
        out[cid] = {
            "n": c["name"], "hp": c.get("hp", ""), "ty": c.get("type", ""),
            "cat": c.get("category", ""), "st": c.get("stage", ""),
            "wk": c.get("weakness", ""), "rs": c.get("resistance", ""),
            "rt": c.get("retreat", ""), "exp": c.get("expansion", ""),
            "ev": c.get("prev_stage", ""),
            "a": [{"n": a["name"], "c": a["cost"], "d": a["damage"], "e": a["effect"]}
                  for a in c.get("attacks", [])],
            "img": cid in have_img,
        }
    return out


